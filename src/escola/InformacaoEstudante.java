    private void enableSorting() {
        TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>((DefaultTableModel) jTTabela.getModel());
        jTTabela.setRowSorter(sorter);

        // Adiciona a capacidade de ordenação pela coluna 0 (ID) de forma ascendente
        sorter.setSortable(0, true);
        sorter.toggleSortOrder(0);
    }
    
