    private void show_table() {
    int CC;

    try {
        con = DriverManager.getConnection("jdbc:mysql://localhost/estudanteinfo", "root", "");
        pst = con.prepareStatement("SELECT * FROM info");
        ResultSet Rs = pst.executeQuery();
        ResultSetMetaData RSMD = Rs.getMetaData();
        CC = RSMD.getColumnCount();
        DefaultTableModel DFT = (DefaultTableModel) jTTabela.getModel();

        DFT.setRowCount(0);
        while (Rs.next()) {
            Vector v2 = new Vector(); // Mova esta linha para dentro do loop

            for (int i = 1; i <= CC; i++) {
                // Adicione os valores à linha
                v2.add(Rs.getString(i));
            }
            DFT.addRow(v2);
        }

    } catch (SQLException ex) {
        Logger.getLogger(InformacaoEstudante.class.getName()).log(Level.SEVERE, null, ex);
    }
}
    
    



